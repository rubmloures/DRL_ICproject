"""
MARL Visualization Module
=========================
Custom Plotly-based visualizations for the MARL Specialist Pipeline.
- KMeans Cluster validation
- MARL Capital Routing over time
- Specialist Convictions over time
"""

import pandas as pd
import numpy as np
import plotly.graph_objects as GO
from plotly.subplots import make_subplots
import plotly.express as px
import os
import logging

logger = logging.getLogger(__name__)

def plot_marl_routing(weights_history, assets, save_dir):
    """
    Plots the capital allocation (weights) generated by the Coordinator over time.
    Stacked Area Chart.
    """
    df = pd.DataFrame(weights_history)
    
    fig = GO.Figure()
    
    # Add an area for each asset, plus Cash
    all_targets = assets + ['Cash']
    for col in all_targets:
        if col in df.columns:
            fig.add_trace(GO.Scatter(
                x=df.index,
                y=df[col],
                hoverinfo='x+y',
                mode='lines',
                line=dict(width=0.5),
                stackgroup='one',
                name=col
            ))
            
    fig.update_layout(
        title="MARL Coordinator: Capital Routing (Softmax VDN)",
        xaxis_title="Time Step",
        yaxis_title="Allocation %",
        hovermode="x unified",
        template="plotly_dark"
    )
    
    filepath = os.path.join(save_dir, "marl_capital_routing.html")
    fig.write_html(filepath)
    logger.info(f"Saved MARL Routing plot to {filepath}")


