def plot_regime_efficacy(audit_log, save_dir):
    """
    Plots the Portfolio Equity colored by market regime extracted by PINN parameters.
    """
    df = pd.DataFrame(audit_log)
    if 'regime' not in df.columns:
        logger.warning("Regime column not found in audit log. Skipping regime plot.")
        return
        
    fig = GO.Figure()
    
    # Base portfolio line
    fig.add_trace(GO.Scatter(
        x=df.index, y=df['global_capital'],
        mode='lines', name='Portfolio Equity', 
        line=dict(color='white', width=1)
    ))
    
    color_map = {
        'stable_trending': 'lime',
        'normal_ranging': 'cyan',
        'elevated_vol': 'yellow',
        'turbulent_shock': 'red',
        'N/A': 'gray',
        'unknown': 'gray'
    }
    
    for regime in df['regime'].unique():
        regime_df = df[df['regime'] == regime]
        fig.add_trace(GO.Scatter(
            x=regime_df.index, y=regime_df['global_capital'],
            mode='markers', name=str(regime).upper(),
            marker=dict(color=color_map.get(regime, 'magenta'), size=6, opacity=0.7)
        ))
        
    fig.update_layout(
        title="Regime Detection Efficacy (Heston-PINN driven)",
        xaxis_title="Execution Steps (Walk-Forward)",
        yaxis_title="Equity ($)",
        template="plotly_dark",
        hovermode="x unified"
    )
    
    filepath = os.path.join(save_dir, "marl_regime_efficacy.html")
    fig.write_html(filepath)
    logger.info(f"Saved Regime Efficacy plot to {filepath}")
