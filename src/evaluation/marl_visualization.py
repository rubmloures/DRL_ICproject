def plot_kmeans_clusters(feature_df, kmeans_model, scaler, save_dir):
    """
    Plots a 3D scatter of the K-Means clusters (nu, xi, rho) for Regime validation.
    """
    if kmeans_model is None or scaler is None:
        logger.warning("K-Means models not provided, skipping cluster plot.")
        return
        
    X = feature_df[['nu', 'xi', 'rho']].values
    X_scaled = scaler.transform(X)
    clusters = kmeans_model.predict(X_scaled)
    
    df_plot = feature_df[['nu', 'xi', 'rho']].copy()
    df_plot['Cluster'] = [str(c) for c in clusters]
    
    fig = px.scatter_3d(
        df_plot, x='nu', y='xi', z='rho',
        color='Cluster',
        title="PINN Heston Parameters: Unsupervised K-Means Regimes",
        opacity=0.7,
        template="plotly_dark"
    )
    
    filepath = os.path.join(save_dir, "kmeans_regime_clusters.html")
    fig.write_html(filepath)
    logger.info(f"Saved KMeans Clusters plot to {filepath}")
    
